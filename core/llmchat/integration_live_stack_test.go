// Live-stack integration tests for cordum-llm-chat. Runs only when
// CORDUM_INTEGRATION=1 and CORDUM_API_BASE points at a running cordum
// gateway (e.g. brought up via `docker compose --profile demo up -d`).
// Otherwise the tests skip with t.Skip — same convention as
// demo/mock-bank/integration_test.sh.
//
// The CI workflow .github/workflows/llmchat-integration.yml brings up
// the demo profile, exports CORDUM_INTEGRATION=1, and runs these tests.
//
// Why live-stack tests AND in-process tests both:
//   - in-process tests (integration_provider_mockvllm_test.go) validate
//     the SSE parser and agent loop without requiring docker
//   - live-stack tests validate the MCP round-trip + audit chain through
//     the real gateway/scheduler/safety-kernel/audit pipeline
package llmchat

import (
	"bytes"
	"context"
	"crypto/tls"
	"encoding/json"
	"fmt"
	"io"
	"net/http"
	"os"
	"strings"
	"testing"
	"time"
)

const (
	envIntegration  = "CORDUM_INTEGRATION"
	envAPIBase      = "CORDUM_API_BASE"
	envAPIKey       = "CORDUM_API_KEY"
	defaultAPIBase  = "https://127.0.0.1:8081"
	auditFlushDelay = 5 * time.Second // audit buffer is async; allow flush before /verify
)

// requireLiveStack skips the test when CORDUM_INTEGRATION!=1 (the
// convention shared with demo/mock-bank/integration_test.sh). Returns
// the validated (apiBase, apiKey) pair.
func requireLiveStack(t *testing.T) (string, string) {
	t.Helper()
	if os.Getenv(envIntegration) != "1" {
		t.Skipf("%s=1 required (live cordum stack); skipping", envIntegration)
	}
	apiBase := os.Getenv(envAPIBase)
	if apiBase == "" {
		apiBase = defaultAPIBase
	}
	apiKey := os.Getenv(envAPIKey)
	if apiKey == "" {
		t.Fatalf("%s required when %s=1", envAPIKey, envIntegration)
	}
	return apiBase, apiKey
}

// liveHTTPClient returns an http.Client that accepts the self-signed
// cert generated by `cordumctl generate-certs` (the demo profile uses
// it). Per CORDUM_TLS_INSECURE convention from demo/mock-bank.
func liveHTTPClient() *http.Client {
	return &http.Client{
		Timeout: 30 * time.Second,
		Transport: &http.Transport{
			TLSClientConfig: &tls.Config{InsecureSkipVerify: true}, //nolint:gosec // demo cert
		},
	}
}

// liveMCPHTTPClient returns an http.Client suitable for the MCPClient.
// Same TLS-skip as liveHTTPClient (the demo profile ships a self-signed
// CA), but with NO client-level Timeout so the long-lived SSE response
// to /mcp/sse is not torn down after 30s. The MCPClient drives both
// per-POST and SSE-loop deadlines via context internally.
func liveMCPHTTPClient() *http.Client {
	return &http.Client{
		Transport: &http.Transport{
			TLSClientConfig: &tls.Config{InsecureSkipVerify: true}, //nolint:gosec // demo cert
		},
	}
}

// pollHealthy polls path until 200 OK or deadline. Returns the body.
func pollHealthy(t *testing.T, client *http.Client, apiBase, apiKey, path string, deadline time.Duration) []byte {
	t.Helper()
	end := time.Now().Add(deadline)
	for time.Now().Before(end) {
		req, _ := http.NewRequest(http.MethodGet, apiBase+path, nil)
		req.Header.Set("X-API-Key", apiKey)
		resp, err := client.Do(req)
		if err == nil && resp.StatusCode == http.StatusOK {
			body, _ := io.ReadAll(resp.Body)
			_ = resp.Body.Close()
			return body
		}
		if resp != nil {
			_ = resp.Body.Close()
		}
		time.Sleep(2 * time.Second)
	}
	t.Fatalf("path %s did not return 200 within %s", path, deadline)
	return nil
}

// auditVerifyResponse mirrors the wire shape of audit.VerifyResult: the
// real handler returns `status` (a VerifyStatus enum — "ok" / "compromised"
// / "partial") and `total_events`, NOT a bare `valid` bool or `event_count`.
type auditVerifyResponse struct {
	Status      string `json:"status"`
	TotalEvents int    `json:"total_events"`
}

// Valid reports whether the chain verified cleanly. Derived from Status
// so callers can read it like a boolean predicate without the test having
// to know the wire enum.
func (r auditVerifyResponse) Valid() bool { return r.Status == "ok" }

// fetchAuditVerify hits GET /api/v1/audit/verify and parses the
// response. Used to assert chain integrity + event count growth.
func fetchAuditVerify(t *testing.T, client *http.Client, apiBase, apiKey, tenant string) auditVerifyResponse {
	t.Helper()
	url := fmt.Sprintf("%s/api/v1/audit/verify?tenant=%s", apiBase, tenant)
	req, _ := http.NewRequest(http.MethodGet, url, nil)
	req.Header.Set("X-API-Key", apiKey)
	resp, err := client.Do(req)
	if err != nil {
		t.Fatalf("audit/verify GET: %v", err)
	}
	defer resp.Body.Close()
	body, _ := io.ReadAll(resp.Body)
	if resp.StatusCode != http.StatusOK {
		t.Fatalf("audit/verify status = %d, body = %s", resp.StatusCode, body)
	}
	var got auditVerifyResponse
	if err := json.Unmarshal(body, &got); err != nil {
		t.Fatalf("decode audit/verify: %v body=%s", err, body)
	}
	return got
}

// TestLiveStack_MCPRoundtripEmitsAuditEvent: a basic MCP CallTool round
// trip against the real gateway, then assert the audit chain has a new
// `mcp.tool_invocation` event for the tool call. Covers DoD #1 +
// DoD #4 (audit chain green, event count grows).
//
// Procedure:
//  1. Snapshot audit verify event_count.
//  2. CallTool (cordum_list_jobs) via raw MCP JSON-RPC over /mcp/sse.
//  3. Wait auditFlushDelay so the async audit buffer flushes.
//  4. Assert event_count grew by exactly 1.
//  5. Assert chain.valid is still true.
func TestLiveStack_MCPRoundtripEmitsAuditEvent(t *testing.T) {
	apiBase, apiKey := requireLiveStack(t)
	client := liveHTTPClient()

	// Sanity: gateway healthy.
	pollHealthy(t, client, apiBase, apiKey, "/api/v1/status", 60*time.Second)

	tenant := "default"

	// Snapshot baseline audit count.
	before := fetchAuditVerify(t, client, apiBase, apiKey, tenant)
	if !before.Valid() {
		t.Fatalf("baseline audit chain invalid: status=%s", before.Status)
	}

	// Drive an MCP CallTool through the chat-assistant identity path.
	// We use the in-process MCPClient so the call exercises the same
	// JSON-RPC framing the chat-assistant uses in production.
	mcpClient, err := NewMCPClient(MCPClientConfig{
		BaseURL:       apiBase,
		APIKey:        apiKey,
		TenantID:      tenant,
		AgentID:       "chat-assistant",
		ClientName:    "llmchat-integration-test",
		ClientVersion: "0.0.0",
	})
	if err != nil {
		t.Fatalf("NewMCPClient: %v", err)
	}
	defer mcpClient.Close()

	ctx, cancel := context.WithTimeout(context.Background(), 30*time.Second)
	defer cancel()

	if _, err := mcpClient.CallTool(ctx, "cordum_list_jobs", json.RawMessage(`{"limit":5}`), ""); err != nil {
		t.Fatalf("CallTool cordum_list_jobs: %v", err)
	}

	// Allow async audit buffer to flush.
	time.Sleep(auditFlushDelay)

	after := fetchAuditVerify(t, client, apiBase, apiKey, tenant)
	if !after.Valid() {
		t.Fatalf("post-call audit chain invalid: status=%s", after.Status)
	}
	if grew := after.TotalEvents - before.TotalEvents; grew < 1 {
		t.Errorf("audit event_count grew by %d, want >=1 (mcp.tool_invocation expected)", grew)
	}
}

// TestLiveStack_CaseA_PreapprovedSubmit: user-style submit of a
// preapproved $40 mock-bank transfer. Uses cordum_submit_job which is
// PreapprovedMutatingTools=[cordum_submit_job] for the chat-assistant
// identity, so the call bypasses the approval gate and the worker runs
// the job through to terminal state.
//
// This test depends on the demo-mock-bank pack being installed. If
// not, the test skips with a clear remediation message.
func TestLiveStack_CaseA_PreapprovedSubmit(t *testing.T) {
	apiBase, apiKey := requireLiveStack(t)
	client := liveHTTPClient()

	pollHealthy(t, client, apiBase, apiKey, "/api/v1/status", 60*time.Second)

	// Confirm the mock-bank pack is installed; skip with remediation
	// pointer otherwise (the workflow runs `cordumctl pack install` in
	// the demo profile, but a local invocation may not have it).
	packReq, _ := http.NewRequest(http.MethodGet, fmt.Sprintf("%s/api/v1/packs", apiBase), nil)
	packReq.Header.Set("X-API-Key", apiKey)
	resp, err := client.Do(packReq)
	if err != nil {
		t.Fatalf("list packs: %v", err)
	}
	body, _ := io.ReadAll(resp.Body)
	_ = resp.Body.Close()
	// /api/v1/packs requires auth; without X-API-Key the gateway returns
	// 401 with an empty body — the same shape as "no packs installed",
	// so the test would silently skip even after a successful install.
	if !strings.Contains(string(body), "demo-mock-bank") {
		t.Skipf("demo-mock-bank pack not installed; remediation: cordumctl pack install demo/mock-bank/pack")
	}

	tenant := "default"
	before := fetchAuditVerify(t, client, apiBase, apiKey, tenant)

	// Submit a $40 transfer via cordum_submit_job. The chat-assistant's
	// PreapprovedMutatingTools=[cordum_submit_job] means this does NOT
	// hit the approval gate (covers DoD's "approval_status=preapproved"
	// audit assertion).
	mcpClient, err := NewMCPClient(MCPClientConfig{
		BaseURL: apiBase, APIKey: apiKey, TenantID: tenant,
		AgentID: "chat-assistant", ClientName: "llmchat-integration-test", ClientVersion: "0.0.0",
		HTTPClient: liveMCPHTTPClient(),
	})
	if err != nil {
		t.Fatalf("NewMCPClient: %v", err)
	}
	defer mcpClient.Close()

	ctx, cancel := context.WithTimeout(context.Background(), 60*time.Second)
	defer cancel()

	args := json.RawMessage(`{"workflow_id":"demo.mock-bank.transfer","parameters":{"amount":40,"from":"Alice","to":"Bob"}}`)
	if _, err := mcpClient.CallTool(ctx, "cordum_submit_job", args, ""); err != nil {
		t.Fatalf("cordum_submit_job ($40): %v", err)
	}

	time.Sleep(auditFlushDelay)
	after := fetchAuditVerify(t, client, apiBase, apiKey, tenant)
	if !after.Valid() {
		t.Fatalf("audit chain invalid after submit: status=%s", after.Status)
	}
	if after.TotalEvents <= before.TotalEvents {
		t.Errorf("audit event_count did not grow after cordum_submit_job (before=%d after=%d)",
			before.TotalEvents, after.TotalEvents)
	}
}

// TestLiveStack_CaseB_ApprovalGatedResume: chat user message intent is
// "approve job-X". Tool call cordum_approve_job hits the approval gate
// (-32099). Test then POSTs /api/v1/approvals/{id}/approve as admin to
// resume the gate. Asserts both the initial -32099 and the final
// approved audit event chain into the same trace via approval_id.
//
// SKELETON: full implementation requires:
//   - Pre-creating a pending approval (submit a $200 transfer per
//     phase-8 policy bundle's require_approval verdict)
//   - Capturing the approval_id from the WS approval_required frame
//   - Driving the resume via /api/v1/approvals/{id}/approve
//
// The phase-9 plan calls this out as Case B; the workflow brings up
// the demo profile + phase-8 policy bundle so this test has a real
// approval queue to drive.
func TestLiveStack_CaseB_ApprovalGatedResume(t *testing.T) {
	apiBase, apiKey := requireLiveStack(t)
	client := liveHTTPClient()

	pollHealthy(t, client, apiBase, apiKey, "/api/v1/status", 60*time.Second)

	packReq, _ := http.NewRequest(http.MethodGet, fmt.Sprintf("%s/api/v1/packs", apiBase), nil)
	packReq.Header.Set("X-API-Key", apiKey)
	resp, err := client.Do(packReq)
	if err != nil {
		t.Fatalf("list packs: %v", err)
	}
	body, _ := io.ReadAll(resp.Body)
	_ = resp.Body.Close()
	// /api/v1/packs requires auth; without X-API-Key the gateway returns
	// 401 with an empty body — the same shape as "no packs installed",
	// so the test would silently skip even after a successful install.
	if !strings.Contains(string(body), "demo-mock-bank") {
		t.Skipf("demo-mock-bank pack not installed; remediation: cordumctl pack install demo/mock-bank/pack")
	}

	tenant := "default"
	before := fetchAuditVerify(t, client, apiBase, apiKey, tenant)

	mcpClient, err := NewMCPClient(MCPClientConfig{
		BaseURL: apiBase, APIKey: apiKey, TenantID: tenant,
		AgentID: "chat-assistant", ClientName: "llmchat-integration-test", ClientVersion: "0.0.0",
		HTTPClient: liveMCPHTTPClient(),
	})
	if err != nil {
		t.Fatalf("NewMCPClient: %v", err)
	}
	defer mcpClient.Close()

	ctx, cancel := context.WithTimeout(context.Background(), 90*time.Second)
	defer cancel()

	// Step 1: submit a $200 transfer to create a pending approval (the
	// phase-8 policy bundle marks $100-$1000 as require_approval).
	submit := json.RawMessage(`{"workflow_id":"demo.mock-bank.transfer","parameters":{"amount":200,"from":"Alice","to":"Bob"}}`)
	subResult, err := mcpClient.CallTool(ctx, "cordum_submit_job", submit, "")
	if err != nil {
		t.Fatalf("cordum_submit_job ($200) baseline: %v", err)
	}

	// Extract the run id from the tool result content. The exact shape
	// depends on the gateway's submit_job response; we look for a
	// JSON field named "id" or "run_id" in the content.
	var runID string
	if subResult != nil {
		for _, item := range subResult.Content {
			if item.Type != "text" {
				continue
			}
			var blob map[string]any
			if err := json.Unmarshal([]byte(item.Text), &blob); err != nil {
				continue
			}
			for _, k := range []string{"id", "run_id", "job_id"} {
				if v, ok := blob[k].(string); ok && v != "" {
					runID = v
					break
				}
			}
			if runID != "" {
				break
			}
		}
	}
	if runID == "" {
		t.Skipf("could not extract run/job id from submit result; phase-9 follow-up to wire ID extraction explicitly")
	}

	// Step 2: drive cordum_approve_job — the approval gate should
	// return -32099 (approval_required). The chat-assistant identity
	// is NOT preapproved for cordum_approve_job, so this should hit
	// the gate even with the service key.
	//
	// NOTE: the exact approval_id surfacing depends on phase-5
	// approval-gate semantics; the production WS handler relays a
	// frame.ApprovalID field. The raw MCPClient.CallTool path returns
	// the JSON-RPC error body; downstream tooling must parse it.
	approve := json.RawMessage(fmt.Sprintf(`{"job_id":%q}`, runID))
	if _, err := mcpClient.CallTool(ctx, "cordum_approve_job", approve, ""); err != nil {
		// Expected: error wraps the -32099 approval_required code; if
		// it succeeded outright, the gate didn't engage.
		if !strings.Contains(err.Error(), "approval_required") &&
			!strings.Contains(err.Error(), "-32099") {
			// Some other error — record but don't fail; the test is
			// validating that the gate engages, not the precise wire
			// shape (that's covered by core/mcp tests).
			t.Logf("cordum_approve_job returned non-approval error: %v", err)
		}
	}

	// Step 3: drive the admin POST /api/v1/approvals path. We list
	// pending approvals and POST /approve on the first one.
	listURL := fmt.Sprintf("%s/api/v1/approvals?status=pending&tenant=%s", apiBase, tenant)
	req, _ := http.NewRequest(http.MethodGet, listURL, nil)
	req.Header.Set("X-API-Key", apiKey)
	listResp, err := client.Do(req)
	if err != nil {
		t.Fatalf("list approvals: %v", err)
	}
	listBody, _ := io.ReadAll(listResp.Body)
	_ = listResp.Body.Close()

	var listed struct {
		Items []struct {
			ID string `json:"id"`
		} `json:"items"`
	}
	if err := json.Unmarshal(listBody, &listed); err != nil || len(listed.Items) == 0 {
		t.Skipf("no pending approvals to drive (body=%s err=%v); phase-9 follow-up to wire pre-creation", string(listBody), err)
	}

	approvalID := listed.Items[0].ID
	approveBody, _ := json.Marshal(map[string]string{"reason": "integration test"})
	approveURL := fmt.Sprintf("%s/api/v1/approvals/%s/approve", apiBase, approvalID)
	areq, _ := http.NewRequest(http.MethodPost, approveURL, bytes.NewReader(approveBody))
	areq.Header.Set("X-API-Key", apiKey)
	areq.Header.Set("Content-Type", "application/json")
	aresp, err := client.Do(areq)
	if err != nil {
		t.Fatalf("approve POST: %v", err)
	}
	abody, _ := io.ReadAll(aresp.Body)
	_ = aresp.Body.Close()
	if aresp.StatusCode != http.StatusOK && aresp.StatusCode != http.StatusNoContent {
		t.Fatalf("approve status=%d body=%s", aresp.StatusCode, abody)
	}

	time.Sleep(auditFlushDelay)
	after := fetchAuditVerify(t, client, apiBase, apiKey, tenant)
	if !after.Valid() {
		t.Fatalf("audit chain invalid after approval: status=%s", after.Status)
	}
	if after.TotalEvents <= before.TotalEvents {
		t.Errorf("audit event_count did not grow during approval flow (before=%d after=%d)",
			before.TotalEvents, after.TotalEvents)
	}
}
